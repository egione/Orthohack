#define OVERFLOW_CHECK 1
/*
PRECISION_RATIO_LIMIT informs Orthohack about the noise level of the incoming data. It's difficult to explain the exact significance, but smaller means greater accuracy, but more coefficients and a slower transform -- perhaps unnecessarily so, if in fact some of the coefficients are only trivially different. Experiment and see where things gel. Must be >=1.0 and, for practical purposes, much closer to 1 than 2. 1.0 provides maximum accuracy at the expense of the performance of both Orthohack and the transform that it generates.
*/
#define PRECISION_RATIO_LIMIT (1+(1.0E-9))
/*
PRECISION_ZERO_LIMIT is the magnitude less than or equal to which an orthobasis sample may be overwritten with 0.0. However, generated ratios may still fall outside the range of this number and its reciprocal. Must be >=0.0 and, for practical purposes, much closer to 0 than 1. 0.0 provides maximum accuracy at the expense of the performance of both Orthohack and the transform that it generates.
*/
#define PRECISION_ZERO_LIMIT (1.0E-11)
/*
SAMPLE_COUNT, >1, is the number of 8-byte (NUMBER_IN)s stored in each file, starting with 000000000.bin . This is the number of spatial samples which are input to the transform. Usually but not always equal to COEFF_COUNT.
*/
#define SAMPLE_COUNT 0x40
