/*
COEFF_COUNT, >1, is the number of real coefficients generated by the transform. For example, an 8x8 real discrete cosine transform generates 0x40 coefficients. This is also the number of files with sequential 9-digit decimal filenames which will be processed, starting with 000000000.bin .
*/
#define COEFF_COUNT 0x40
